/*===============================================================
	@brief      二值化图像解压
	@function   解压图像的同时，将行均匀提取，取出320*64的二值图像数组
        @parameter1 图像解压目的地址
        @parameter2 图像解压源地址
        @note       方法有多种，也可解压和提取分开进行，找出用时最少的方法
==================================================================*/

void img_extract_for_64x(uint8 *dst, uint8 (*src)[CAMERA_ROW_DMA_NUM])          //1.0ms
{
    //注：山外的摄像头 0 表示 白色，1表示 黑色
    register uint8 tmpsrc;
    register uint8 *tmpsrc_Row;
    register uint8 i, j; 
    
    for (i=0; i<64; i++)
    {
        //tmpsrc_Row = src[(((uint32)i*CAMERA_H)/IMAGE_HEIGHT)];
      
        tmpsrc_Row = src[g_row[i]]; 
        for (j=0; j<CAMERA_ROW_DMA_NUM; j++)
        {
            tmpsrc = ~(*tmpsrc_Row);            //为了程序执行效率考虑
            tmpsrc_Row++;                       //使用指针代替数组寻址运算
            *dst++ = ((tmpsrc >> 7 ) & 0x01);
            *dst++ = ((tmpsrc >> 6 ) & 0x01);
            *dst++ = ((tmpsrc >> 5 ) & 0x01);
            *dst++ = ((tmpsrc >> 4 ) & 0x01);
            *dst++ = ((tmpsrc >> 3 ) & 0x01);
            *dst++ = ((tmpsrc >> 2 ) & 0x01);
            *dst++ = ((tmpsrc >> 1 ) & 0x01);
            *dst++ = ((tmpsrc >> 0 ) & 0x01); 
            
//            *dst++ = ((tmpsrc >> 1 ) & 0x01);
//            *dst++ = ((tmpsrc >> 0 ) & 0x01);
//            *dst++ = ((tmpsrc >> 3 ) & 0x01);
//            *dst++ = ((tmpsrc >> 2 ) & 0x01);
//            *dst++ = ((tmpsrc >> 5 ) & 0x01);
//            *dst++ = ((tmpsrc >> 4 ) & 0x01);
//            *dst++ = ((tmpsrc >> 7 ) & 0x01);
//            *dst++ = ((tmpsrc >> 6 ) & 0x01); 
        }
    }
}

/*===============================================================
	@brief      数组提取
	@function   进行列提取，320取出128列
        @parameter1 图像目的地址
        @parameter2 图像源地址
        @note       方法有多种，也可解压和提取分开进行，找出用时最少的方法
==================================================================*/

void img_rowcol_128X64(uint8 dst[IMAGE_HEIGHT][IMAGE_WIDTH], uint8 (*src)[CAMERA_W])    
{
    register uint8 *tmpdst = (uint8*)dst;
    register uint8 *tmpsrc = src[0];
    register uint8 i = 0, j = 0;
    for(i = 0; i < IMAGE_HEIGHT ;i++)
    {
        tmpsrc = src[i];
        //tmpdst = dst[i];
        tmpdst = dst[i];
        for(j = 0; j < IMAGE_WIDTH;j++)
        {
           *tmpdst = tmpsrc[(((uint32)j*CAMERA_W)/IMAGE_WIDTH)]; //不取列
           // *tmpdst = tmpsrc[g_RowCol[i][IMAGE_WIDTH-1-j]];
          //dst[IMAGE_HEIGHT-1-i][j] = src[i][g_RowCol[i][j]];//2.5ms
            tmpdst++;                                   //使用指针代替数组寻址运算
        }
    }
}

